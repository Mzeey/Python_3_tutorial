import math

val = 3

sqrt_val = math.sqrt(val)
print(round(sqrt_val**2))


expVal = math.exp(val)
print(expVal)

#Factorial of val
facVal = math.factorial(val)
print(facVal)

sinVal = math.sin(val) #results are in radients
print(sinVal)

floorVal = math.floor(sinVal)
print(floorVal)

ceilVal = math.ceil(sinVal)
print(ceilVal)